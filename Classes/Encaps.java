
public class Encaps {
	private int fon;
	private int age;
	private String place;

	public int getfon() {
		return fon;
	}

	public void setfon(int fon) {
		this.fon = fon;
	}

	public int getage() {
		return age;
	}

	public void setage(int age) {
		this.age = age;
	}

	public String getplace() {
		return place;
	}

	public void setplace(String place) {
		this.place = place;
	}

}
